public class Laboratory {
	
	private int id;
	private String name;//实验室名称
	private String academybelong;//归属学院
	private String address;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAcademybelong() {
		return academybelong;
	}
	public void setAcademybelong(String academybelong) {
		this.academybelong = academybelong;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	

}
